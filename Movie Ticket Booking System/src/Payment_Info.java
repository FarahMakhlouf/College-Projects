    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (!Card_Num.getText().equals("") && !Card_Holder.getText().equals(""))
        {              
        if(Login.Current_Customer.getCard_Number().equals(Card_Num.getText()))
        {
            try {
                Payment e = new Payment(Price,Card_Type.getSelectedItem().toString(),Card_Holder.getText(),Move_Name.getText(),Theatre_Name.getText());
                Save(e);
                JOptionPane.showMessageDialog(rootPane, "Successful Payment","Done",JOptionPane.INFORMATION_MESSAGE);
                int option = JOptionPane.OK_CANCEL_OPTION;
                int result = JOptionPane.showConfirmDialog(rootPane, "Do you want to Book another ticket?","",option);
                if (result == JOptionPane.OK_OPTION)
                {
                City Form=new City();
                this.hide();
                Form.setVisible(true);
                }
                else
                    exitActionPerformed(evt);
                
            } catch (ParserConfigurationException ex) {
                Logger.getLogger(Payment_Info.class.getName()).log(Level.SEVERE, null, ex);
            } catch (TransformerException ex) {
                Logger.getLogger(Payment_Info.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            else
            {
            JOptionPane.showMessageDialog(rootPane, "Check Card Number","Error",JOptionPane.INFORMATION_MESSAGE);
           }
        }
        else if(Card_Num.getText().equals("") || Card_Holder.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please Fill all details","Error",JOptionPane.INFORMATION_MESSAGE); 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

