    public Payment_Info(int MovieIndex,int TheaterIndex,int ShowIndex,int Num_Tickets) {
        initComponents();
        Move_Name.setText(City.Movies.get(MovieIndex).getMovie_Name());
        Theatre_Name.setText(City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getTheater_Name());
        Time.setText(City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getShows().get(ShowIndex).getTime());
        Type.setText(City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getShows().get(ShowIndex).getType());
        username.setText(Login.Current_Customer.getUsername());
        switch (City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getShows().get(ShowIndex).getType()) {
            case "Regular":
                Price=City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getRegular_Price()*Num_Tickets;
                jLabel5.setText(String.valueOf(Price) + "LE");
                break;
            case "3D":
                Price=City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getThreeDim_Price()*Num_Tickets;
                jLabel5.setText(String.valueOf(Price) + "LE");
                break;
            case "4D":
                Price=City.Movies.get(MovieIndex).getTheaters().get(TheaterIndex).getFourDim_Price()*Num_Tickets;
                jLabel5.setText(String.valueOf(Price) + "LE");
                break;
        }
    }
    
    public void Save(Payment e)
    {
        try {
            List<Payment> p = Login.customers.get(Login.CurrentCustomerIndex).getPayments();
            p.add(e);
            Login.customers.get(Login.CurrentCustomerIndex).setPayments(p);
            Customer.SaveCustomers(Login.customers);
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(Payment_Info.class.getName()).log(Level.SEVERE, null, ex);
        } catch (TransformerException ex) {
            Logger.getLogger(Payment_Info.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
        
