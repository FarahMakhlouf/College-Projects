    private void Movies_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Movies_TableMouseClicked
        // TODO add your handling code here:
        int MovieIndex=0;
        int tableindex = Movies_Table.getSelectedRow();
        for (int j = 0;j<Movies.size();j++)
        {
            if (Movies_Table.getModel().getValueAt(tableindex,0).equals(Movies.get(j).getMovie_Name()) && Movies.get(j).getCity().equals(jComboBox1.getSelectedItem().toString()) && Movies.get(j).getCategory().equals(jComboBox2.getSelectedItem().toString()))
            {
                MovieIndex=j;
                break;
            }
        }
        Movie_Info Movie_info=new Movie_Info(MovieIndex);
        this.hide();
        Movie_info.setVisible(true);
    }//GEN-LAST:event_Movies_TableMouseClicked

