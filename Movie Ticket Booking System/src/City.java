    public void Display(String City,String category)
    {
        SelectedMovies=Movie.ReadMovies(City, category);
        DefaultTableModel Model = (DefaultTableModel)Movies_Table.getModel();
        Object[] row=new Object[2];
        for (int i=0;i<SelectedMovies.size();i++) 
        {
            row[0] = SelectedMovies.get(i).getMovie_Name();
            row[1] = SelectedMovies.get(i).getRelease_Date();
            Model.addRow(row);
        }
    }
